package day1;

public class Sample2 {

	public static void main(String[] args) {
		// 숫자 연산
		// 자바의 사칙연산은 실생활에서 일반적으로 사용하는 사칙연산과 다를 바가 없다. 
		// 자바는  +,-,*,/ 기호를 이용하여 두 숫자간 사칙연산을 수행한다. 
		
		
		int a = 10;
		int b = 5;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
	

	}

}
