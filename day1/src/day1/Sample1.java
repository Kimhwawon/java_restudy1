package day1;

public class Sample1 {
	public static void main(String[] args) {
		// 정수 
		// 자바의 정수를 표현하기 위한 자료형은 int, long
		// ( byte, short등도 있지만 잘 사용하지 않는다. )
		// int와 long의 차이는 표현할 수 있는 숫자의 범위이다. 
		 int age = 10;
		 long countOfStar = 8764827384923849L;
		 
		 // 실수
		 // 자바의 실수를 표현하기 위한 자료형은 float, double 이다. 
		 // float와 double의 차이 역시표현할 수 있는 숫자의 범위이다. 
		 float pi = 3.14F;
		 double morePi = 3.14159265358979323846;
		 // 자바에서 실수형은 디폴트가 double이므로 위의 에에서 보듯이 float
		 // 변수에 값을 대입할 때에는 3.14F와 같이 F 접미사(또는 소문자 f)를
		 // 꼭 붙여 주어야 한다. 
		 // float 변수에 값을 대입할 때에는 접미사를 누락하면 컴파일 에러가 발생
		 
		 // 또는 과학적 지수 표현식으로 다음과 같이 사용할 수 있다.
		 double d1 = 123.4;
		 double d2 = 1.234e2;
		 
}
	

	
	
	
	
	
	
	
}
