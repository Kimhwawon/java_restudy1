		// 불 여산은 참, 거짓을 판단하는 연산을 말한다. 예를 들어 다음과 같은 불 연산 들이 있다.
		
//		2 > 1 // 참
//		1 == 2// 거짓
//		3 % 2 == 1 // 참 (3을 2로 나눈 나머지는 1이므로 참이다. )
	//	"3".equals("2"); // 거짓
		
